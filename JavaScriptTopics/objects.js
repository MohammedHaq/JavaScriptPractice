//objects in JavaScript act like Objects in any other programming language
//objects in JavaScript behave similarly to objects in Python

//an example of creating an object is the following

var ourDog = {
    "name" : "Camper",
    "legs" : 4,
    "tails" : 1,
    "friends" : ["Everything"]
}

// to access a property in an object, we do the following. 

var dogName = ourDog.name;
var dogLegs = ourDog.legs;

// we can also access object properties with the following

var dogTails = ourDog["tails"]
var dogFriends = ourDog["friends"]

