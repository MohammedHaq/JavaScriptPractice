// we can use object as dictionaries and use them to search for a certain value
// an example of this is the following:

var lookup = {
    "alpha" : "Adams",
    "bravo" : "Boston",
    "charlie" : "Chicago",
}

// let's say, we want to find the value of charlie in the given object / dictionary
// we do that by doing the following

result = lookup.charlie
