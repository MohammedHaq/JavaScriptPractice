// we can do a similar thing to push but instead we can remove the last item in the array using the pop() function in JavaScript
// For example, if we want to pop the number 3 from the end of an array, we do it as the following

var MyArray = [1,2,3];
MyArray.pop()

console.log(MyArray)

//when using the pop function, you do not have to automatically specify the end most item in the array. it will automatically remove the last item in the array for you

