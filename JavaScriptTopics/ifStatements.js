//if statements in JavaScript are written as the following
/*

if (parameter to check is true) {

    code

}

*/

//for example

var Array = [1,2,3,4,5,6]
if (Array[0] == 1) {
    console.log("Hello World!")
}

//checks to see if the statement matches the condition, if it is, then it prints Hello World!


// to use the AND operator in JavaScript, the syntax is as follows

Array2 = [7,8,9]
if (Array[0] == 1 && Array2[0] == 7) {
    console.log("Hello World! Again")
}

// The AND Operator is denoted using two && symbols


// to use the OR operator in JavaScript the syntax is as follows
if (Array[0] == 2 || Array2[0] == 7) {
    console.log("Hello World for the third time!")
}